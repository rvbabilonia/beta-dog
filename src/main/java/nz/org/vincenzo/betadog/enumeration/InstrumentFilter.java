package nz.org.vincenzo.betadog.enumeration;

/**
 * The enumeration of instrument filters.
 *
 * @author Rey Vincent Babilonia
 */
public enum InstrumentFilter {

    /**
     * All instruments.
     */
    ALL,
    /**
     * Ordinary shares, preference shares, options, equity warrants, convertible notes
     * and some unit investment trust funds.
     */
    EQUITIES,
    /**
     * Exchange-traded or index funds with some unit investment trust funds.
     */
    FUNDS
}
